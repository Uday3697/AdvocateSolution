              <p style="text-align:justify; font-weight:bold;"><img src="../images/hzbhistory.jpg" class="img-responsive" align="left">In ancient times the district was covered with inaccessible forests inhabited by tribes who remained independent. The entire territory of Chhotanagpur, known as Jharkhand (meaning forest territory) was presumably beyond the pale of outside influence in ancient India. Throughout the Turko-Afghan period (up to 1526 CE), the area remained virtually free from external influence. It was only with the accession of Akbar to the throne of Delhi in 1556 that Muslim influence penetrated Jharkhand, then known to the Mughals as Kokrah. In 1585, Akbar sent a force under the command of Shahbaj Khan to reduce the Raja of Chotanagpur to the position of a tributary. After the death of Akbar in 1605, the area presumably regained its independence. This necessitated an expedition in 1616 by Ibrahim Khan Fateh Jang, the Governor of Bihar and brother of Queen Noorjehan. Ibrahim Khan defeated and captured Durjan Sal, the 46th Raja of Chotanagpur. He was imprisoned for 12 years but was later released and reinstated on the throne after he had shown his ability in distinguishing a real diamond from a fake one.</p>
              
<p style="text-align:justify;font-weight:bold;">In 1632 CE, Chotanagpur was given as Jagir to the Governor at Patna for an annual payment of Rs.136,000. This was raised to Rs.161,000 in 1636 CE. During the reign of Muhammad Shah (1719–1748), Sarballand Khan, the Governor of then Bihar, marched against the Raja of Chotanagpur and obtained his submission. Another expedition was led by Fakhruddoula, the Governor of Bihar in 1731.
He came to terms with the Raja of Chotanagpur. In 1735 Alivardi Khan had some difficulty in enforcing the payment of the annual tribute of Rs. 12000 from the Raja of Ramgarh, as agreed to by the latter according to the terms settled with Fakhruddoula.</p>
<p style="text-align:justify; font-weight:bold;">
This situation continued until the occupation of the country by the British. During the Muslim period, the main estates in the district were Ramgarh, Kunda, Chai and Kharagdiha. Subsequent to the Kol uprising in 1831 that, however, did not seriously affect Hazaribag, the administrative structure of the territory was changed. The paraganas Ramgarh, Kharagdiha, Kendi and Kunda became parts of the South-West Frontier Agency and were formed into a division named Hazaribag as the administrative headquarters.
In 1854 the designation of South-West Frontier Agency was changed to Chota Nagpur and it began to be administered as a Non-regulation province under the Lieutenant Governor of the then Bihar. In 1855-56 there was the great uprising of the Santhals against the British but was brutally suppressed.
After 1991 census, the district of Hazaribag has been divided into three separate districts, viz., Hazaribag, Chatra and Koderma. The two sub-divisions namely Chatra and Koderma were upgraded to the status of independent districts.
During British rule, one had go by train to Giridih and then travel in a vehicle called push-push to Hazaribagh. It was pushed and pulled by human force over hilly tracts. It was exciting journey across rivers and through dense forests infested with bandits and wild animals. Rabindranath Tagore travelled in a push-push along the route in 1885. He has recorded the experience in an essay, Chotanagpur families. When the Grand Chord was opened in 1906, Hazaribagh Road station was linked with the town. For many years, Lal Motor Company operated the rail-cum-bus service between Hazaribagh town and Hazaribagh Road station.</p>

<p style="text-align:justify; font-weight:bold;">  
The town became a cantonment in 1790, the Ramgarh battalion having been raised ten years earlier. It was then part of Ramgarh district. It became a district headquarter in 1834. The cantonment flourished till 1884.This resulted in a planned old city. This part of the town is known as Boddam Bazar, after the officer who laid it out. Many Englishmen settled in Hazaribagh during the British period. They built large bungalow type houses, quite often with sloping roofs. Many of them were great hunters and hunting stories abound in the town by word of mouth. Most of them left after independence. Tutu Imam topped the list of hunting legends in the town along with Prof Rajendra Pandey. A century back it was common for tigers and leopards to poach upon livestock in the outskirts of the town.
Hazaribagh Central Jail housed many leaders of the Indian freedom movement, including Dr. Rajendra Prasad, later the first president of India. The popular leader Jayaprakash Narayan was put under arrest in this jail during the Quit India Movement of 1942. His escape from this high security prison(with the help of 53 dhotis to cross the wall of jail) and the support he received from the local people is one of the legends of the Indian Independence movement.
During the early years of World War II an internment camp ("parole camp") for German civilians was in the town. In June 1942 it housed 36 women, 5 men and 16 children. 21 females with 13 children, had been transferred on 25. February 1942 from Diyatalawa. In autumn they were transferred to the family camps at Purandhar or Satara.


